# NSMChat Class
# ══════════════════════════════════════════════════════════════════
class NSMChat:
    """
    محادثة ذكية v2:
    - E (784×128) مُدرَّبة: فصل 90.5% بين المواضيع
    - بحث مزدوج: keyword + embedding cosine
    - ذاكرة الجلسة
    """

    def __init__(self):
        # تحميل Embedding المُدرَّبة
        if _EMBED_PATH.exists():
            data = np.load(_EMBED_PATH)
            self.E = data["E"].astype(np.float32)
            print(f"✓ Embedding محملة: {self.E.shape} (مُدرَّبة)")
        else:
            print("⚠ nsm_embedding.npz غير موجود — شغّل nsm_trainer.py أولاً")
            print("  سيُستخدم embedding عشوائي مؤقت")
            rng = np.random.default_rng(42)
            self.E = rng.normal(0, 1/28, (784, 128)).astype(np.float32)

        # NLP اختياري
        self.nlp = None
        if _HAS_NLP:
            try:
                self.nlp = ArabicNLPEngine(ckg=None)
                print("✓ Arabic NLP محمّل")
            except Exception:
                pass

        # تجهيز متجهات المواضيع مسبقاً
        self._topic_vecs = {}
        for entry in _KB:
            ev = self._embed(entry["e"] + " " + " ".join(entry["k"]))
            self._topic_vecs[entry["t"]] = ev

        # ذاكرة الجلسة الذكية
        self.history: List[Tuple[str, str]] = []
        self._last_topic = ""
        if _HAS_MEMORY:
            self.memory = ConversationMemory()
            print("✓ ذاكرة المحادثة مُفعَّلة")
        else:
            self.memory = None

        print("✓ NSM Chat v2 جاهز!\n")

    # ── Encoding ──────────────────────────────────────────────────
    def _encode_raw(self, text: str) -> np.ndarray:
        if self.nlp:
            try:
                r = self.nlp.analyse(text)
                return np.array(r.feature_vector.to_list(), dtype=np.float32)
            except Exception:
                pass
        return text_to_vec(text)

    def _embed(self, text: str) -> np.ndarray:
        x = self._encode_raw(text)
        z = self.E.T @ x
        return l2(z)

    # ── البحث ─────────────────────────────────────────────────────
    def _find(self, text: str) -> Tuple[str, float]:
        q_emb = self._embed(text)
        tl_norm = normalize_ar(text)
        tl = text.lower()

        best_ans, best_score = "", -1.0

        for entry in _KB:
            # 1) Keyword score
            kw = sum(1.0 for k in entry["k"] if k in tl or k in tl_norm)
            kw_score = min(kw / max(len(entry["k"]), 1), 1.0)

            # 2) Embedding cosine
            emb_score = (cosine(q_emb, self._topic_vecs[entry["t"]]) + 1.0) / 2.0

            # 3) Context bonus: إذا نفس موضوع الرسالة السابقة
            ctx_bonus = 0.1 if entry["t"] == self._last_topic else 0.0

            total = 0.55 * kw_score + 0.40 * emb_score + 0.05 * ctx_bonus

            if total > best_score:
                best_score = total
                best_ans = entry["answer"] if "answer" in entry else entry["a"]
                self._last_topic = entry["t"]

        return best_ans, best_score

    # ── المحادثة ──────────────────────────────────────────────────
    def chat(self, user_input: str) -> str:
        if not user_input.strip():
            return "الرجاء كتابة سؤالك."

        # ── NSM Agent الذكي (Groq) — أولوية 1 للطلبات الطبيعية ──
        # يلتقط كل طلب يبدأ بكلمة trigger بدون اشتراط صيغة معينة
        if _HAS_NSM_AGENT and _nsm_agent and any(
            user_input.strip().startswith(t) for t in _AGENT_TRIGGERS
        ):
            response = _nsm_agent.run(user_input)
            self._last_source = "nsm_agent:groq"
            self.history.append((user_input, response))
            return response

        # ── Code Agent المباشر — أولوية 2 للأوامر الدقيقة (افحص/قائمة/ارفع/عدل path|old|new) ──
        agent_response = _handle_code_command(user_input)
        if agent_response is not None:
            self._last_source = "code_agent"
            self.history.append((user_input, agent_response))
            return agent_response

        # تغنية الاستعلام بالسياق إذا لزم
        query = user_input
        if self.memory and self.memory.needs_context(user_input):
            query = self.memory.enrich_query(user_input)

        answer, score = self._find(query)

        # ── LLM Fallback: إذا الثقة منخفضة أو السؤال خارج القاموس ──
        # score < 0.45 → القاموس غير واثق → نفوّض للـ LLM
        if score < 0.45 and _HAS_LLM and _llm_instance is not None:
            try:
                # نمرر تاريخ المحادثة للسياق
                history_for_llm = [
                    {"role": "user", "content": u} if i % 2 == 0
                    else {"role": "assistant", "content": a}
                    for i, (u, a) in enumerate(self.history[-4:])
                ]
                llm_result = _llm_instance.generate(query, history=history_for_llm)
                answer = llm_result.text
                # نضيف إشارة المصدر للواجهة (اختياري)
                self._last_source = f"llm:{llm_result.provider.value}"
            except Exception as e:
                # إذا فشل LLM نرجع للقاموس أو رسالة افتراضية
                if score < 0.12:
                    answer = (
                        "سؤالك مثير للاهتمام! يمكنني المساعدة في: "
                        "الإسلام والقرآن، الذكاء الاصطناعي، الرياضيات، "
                        "اللغة العربية، التاريخ، الصحة، والبرمجة."
                    )
                self._last_source = "kb:fallback_error"
        elif score < 0.12:
            # لا LLM ولا قاموس → رسالة افتراضية
            answer = (
                "سؤالك مثير للاهتمام! يمكنني المساعدة في: "
                "الإسلام والقرآن، الذكاء الاصطناعي، الرياضيات، "
                "اللغة العربية، التاريخ، الصحة، والبرمجة."
            )
            self._last_source = "kb:low_confidence"
        else:
            self._last_source = f"kb:{self._last_topic}"

        # حفظ في الذاكرة
        if self.memory:
            self.memory.add(user_input, answer, self._last_topic)
        self.history.append((user_input, answer))
        return answer

    def last_source(self) -> str:
        """من أين جاء الرد الأخير: 'kb:...' أو 'llm:...'"""
        return getattr(self, "_last_source", "kb:unknown")

    def clear_history(self):
        self.history.clear()
        self._last_topic = ""
        if self.memory:
            self.memory.clear()

    def get_history(self) -> List[Tuple[str, str]]:
        return list(self.history)

    def context_info(self) -> str:
        """معلومات السياق الحالي للعرض في الواجهة"""
        if self.memory:
            return self.memory.context_summary()
        return ""



