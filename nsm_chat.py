def _load_weights(path: Path) -> np.ndarray:
    rows = []
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            vals = [float(x) for x in line.split(",")]
            if len(vals) < 784: vals += [0.0]*(784-len(vals))
            elif len(vals) > 784: vals = vals[:784]
            rows.append(vals)
    W = np.array(rows, dtype=np.float32)
    if W.shape[0] < 784:
        W = np.vstack([W, np.zeros((784-W.shape[0], 784), dtype=np.float32)])
    return W[:784, :784]

# ══════════════════════════════════════════════════════════════════
# 2) Self-Attention
# ══════════════════════════════════════════════════════════════════
class SimpleAttention:
    def __init__(self, dim=784, heads=4, seed=42):
        rng = np.random.default_rng(seed)
        self.dim, self.heads = dim, heads
        self.head_dim = dim // heads
        s = math.sqrt(2.0/(dim+self.head_dim))
        self.Wq = rng.normal(0,s,(heads,dim,self.head_dim)).astype(np.float32)
        self.Wk = rng.normal(0,s,(heads,dim,self.head_dim)).astype(np.float32)
        self.Wv = rng.normal(0,s,(heads,dim,self.head_dim)).astype(np.float32)
        self.Wo = rng.normal(0,s,(heads*self.head_dim,dim)).astype(np.float32)

    def forward(self, x: np.ndarray) -> np.ndarray:
        out_heads = []
        for h in range(self.heads):
            q = self.Wq[h].T @ x
            k = self.Wk[h].T @ x
            v = self.Wv[h].T @ x
            score = float(q @ k) / math.sqrt(self.head_dim)
            attn = 1.0/(1.0+math.exp(-score))
            out_heads.append(attn*v)
        concat = np.concatenate(out_heads)
        out = self.Wo.T @ concat + x
        mu, sigma = out.mean(), out.std()+1e-8
        return (out-mu)/sigma

# ══════════════════════════════════════════════════════════════════
# 3) Tokenizer
# ══════════════════════════════════════════════════════════════════
def _fnv1a(s):
    h = 0x811c9dc5
    for ch in s:
        h ^= ord(ch)
        h = (h*0x01000193)&0xFFFFFFFF
    return h

def text_to_vector(text: str, dim=784) -> np.ndarray:
    vec = np.zeros(dim, dtype=np.float32)
    t = re.sub(r'\s+',' ', text.strip().lower())
    for n in (1,2,3):
        for i in range(len(t)-n+1):
            vec[_fnv1a(t[i:i+n])%dim] += 1.0
    total = vec.sum()
    if total > 0:
        vec = np.log1p(vec*10.0/total)/math.log1p(10.0)
    return vec.astype(np.float32)

