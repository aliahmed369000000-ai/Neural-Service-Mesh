def _kw_score(text: str, keywords: List[str]) -> float:
    t = text.lower()
    score = sum(1.0 for kw in keywords if kw in t)
    return min(score / max(len(keywords),1), 1.0)

class NSMChat:
    def __init__(self, weights_path: Path = _WEIGHTS_PATH):
        if weights_path.exists():
            self.W = _load_weights(weights_path)
        else:
            self.W = np.random.default_rng(42).normal(0,0.1,(784,784)).astype(np.float32)
        self.attention = SimpleAttention()
        self.nlp = ArabicNLPEngine(ckg=None) if _HAS_NLP else None
        self.history: List[Tuple[str,str]] = []

    def _encode(self, text: str) -> np.ndarray:
        if self.nlp:
            try:
                r = self.nlp.analyse(text)
                return np.array(r.feature_vector.to_list(), dtype=np.float32)
            except Exception:
                pass
        return text_to_vector(text)

    def _neural(self, vec: np.ndarray) -> np.ndarray:
        out = self.W @ vec
        out = np.maximum(0, out)
        return self.attention.forward(out)

    def _cosine(self, a: np.ndarray, b: np.ndarray) -> float:
        n = (np.linalg.norm(a)*np.linalg.norm(b))+1e-8
        return (float(a@b)/n+1.0)/2.0

    def chat(self, user_input: str) -> str:
        if not user_input.strip():
            return "الرجاء كتابة سؤالك."
        vec = self._encode(user_input)
        neural_out = self._neural(vec)
        best, best_score = "", -1.0
        for entry in _KNOWLEDGE_BASE:
            kw = _kw_score(user_input, entry["keywords"])
            topic_vec = self._neural(text_to_vector(entry["topic"]+" "+" ".join(entry["keywords"])))
            cos = self._cosine(neural_out, topic_vec)
            total = 0.65*kw + 0.35*cos
            if total > best_score:
                best_score, best = total, entry["answer"]
        if best_score < 0.12:
            best = (f"سؤالك عن '{user_input[:40]}' يحتاج إلى معرفة أعمق. "
                    "يمكنني المساعدة في: القرآن، الإسلام، العلوم، التكنولوجيا، "
                    "اللغة العربية، التاريخ، الصحة، والتعليم.")
        self.history.append((user_input, best))
        return best

    def clear_history(self): self.history.clear()
    def get_history(self): return list(self.history)

def main():
    print("="*55)
    print("  NSM Chat — ذكاء بلا قاعدة بيانات")
    print("="*55)
    bot = NSMChat()
    print("اكتب سؤالك. اكتب 'خروج' للإنهاء.\n")
