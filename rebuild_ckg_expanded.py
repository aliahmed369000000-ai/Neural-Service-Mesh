# Normalise all keywords once
CONCEPT_MAP = {}
for cluster, concepts in RAW_CLUSTERS.items():
    for concept, keywords in concepts.items():
        CONCEPT_MAP[concept] = {
            "cluster":  cluster,
            "keywords": [norm(k) for k in keywords],
        }

