# Load Quran
# ══════════════════════════════════════════════════════════════════════════
print("\nLoading Quran...")
data = json.load(open('knowledge_sources/quran/data/quran.json'))
surahs = data['surahs']
if isinstance(surahs, dict):
    surahs = surahs.get('references', list(surahs.values()))

texts_raw, texts_norm, refs = [], [], []
for s in surahs:
    s_num = s.get('number', 0)
    for a in s.get('ayahs', []):
        txt = a.get('text', '').strip()
        if txt:
            texts_raw.append(txt)
            texts_norm.append(norm(txt))
            refs.append(f"quran:{s_num}:{a.get('numberInSurah', 0)}")

print(f"Loaded {len(texts_norm)} ayahs")

# ══════════════════════════════════════════════════════════════════════════
# Scan ayahs for concepts
# ══════════════════════════════════════════════════════════════════════════
print("\nScanning for concepts...")
concept_freq    = defaultdict(int)
concept_sources = defaultdict(set)
ayah_concepts   = []   # list of sets, one per ayah

for i, (txt, ref) in enumerate(zip(texts_norm, refs)):
    found = set()
    for concept, info in CONCEPT_MAP.items():
        for kw in info["keywords"]:
            if kw in txt:
                found.add(concept)
                break
    ayah_concepts.append(found)
    for c in found:
        concept_freq[c] += 1
        concept_sources[c].add(ref)

print(f"Concepts matched: {len(concept_freq)}")

# Filter: keep only concepts that appear in >= 2 ayahs
MIN_FREQ = 2
concept_freq    = {k: v for k, v in concept_freq.items() if v >= MIN_FREQ}
concept_sources = {k: v for k, v in concept_sources.items() if k in concept_freq}
print(f"After min_freq={MIN_FREQ}: {len(concept_freq)} concepts")

# ══════════════════════════════════════════════════════════════════════════
# Build concepts_db
# ══════════════════════════════════════════════════════════════════════════
max_freq = max(concept_freq.values()) if concept_freq else 1

concepts_db = {}
for concept, freq in concept_freq.items():
    cluster  = CONCEPT_MAP[concept]["cluster"]
    strength = round(math.log(freq + 1) / math.log(max_freq + 1), 4)
    concepts_db[concept] = {
        "name":       concept,
        "cluster":    cluster,
        "sources":    sorted(concept_sources[concept])[:30],
        "frequency":  freq,
        "strength":   strength,
        "first_seen": _NOW(),
        "last_seen":  _NOW(),
    }

# ══════════════════════════════════════════════════════════════════════════
# Build relations — co-occurrence within same ayah
# ══════════════════════════════════════════════════════════════════════════
print("\nBuilding co-occurrence relations...")
co_occur = defaultdict(lambda: {"count": 0, "evidence": []})

for i, found in enumerate(ayah_concepts):
    # Only concepts that survived the min_freq filter
    active = [c for c in found if c in concept_freq]
    ref = refs[i]
    active_sorted = sorted(active)
    for j in range(len(active_sorted)):
        for k in range(j + 1, len(active_sorted)):
            a, b = active_sorted[j], active_sorted[k]
            key  = f"{a}||{b}"
            co_occur[key]["count"] += 1
            if len(co_occur[key]["evidence"]) < 5:
                co_occur[key]["evidence"].append(ref)

print(f"Raw pairs: {len(co_occur)}")

# ── Co-occurrence relations (count >= 3) ──
relations_db = {}
MIN_CO = 3
for key, info in co_occur.items():
    if info["count"] < MIN_CO:
        continue
    a, b     = key.split("||")
    freq_a   = concept_freq.get(a, 1)
    freq_b   = concept_freq.get(b, 1)
    weight   = round(info["count"] / min(freq_a, freq_b), 4)
    weight   = min(weight, 1.0)
    rel_key  = f"{a}→{b}"
    relations_db[rel_key] = {
        "source":        a,
        "target":        b,
        "weight":        weight,
        "relation_type": "co_occurrence",
        "evidence":      info["evidence"],
        "count":         info["count"],
        "first_seen":    _NOW(),
        "last_seen":     _NOW(),
    }

print(f"Co-occurrence relations (count>={MIN_CO}): {len(relations_db)}")

# ── Semantic/cluster relations ────────────────────────────────────────────
print("Adding intra-cluster semantic relations...")
cluster_groups = defaultdict(list)
for concept in concepts_db:
    cluster_groups[CONCEPT_MAP[concept]["cluster"]].append(concept)

sem_added = 0
for cluster, members in cluster_groups.items():
    for j in range(len(members)):
        for k in range(j + 1, len(members)):
            a, b    = sorted([members[j], members[k]])
            rel_key = f"{a}→{b}"
            if rel_key not in relations_db:
                # Both must have freq >= 5 for a semantic link
                if concept_freq.get(a, 0) >= 5 and concept_freq.get(b, 0) >= 5:
                    relations_db[rel_key] = {
                        "source":        a,
                        "target":        b,
                        "weight":        round(0.3 + 0.2 * min(
                                             concept_freq.get(a,1),
                                             concept_freq.get(b,1)) / max_freq, 4),
                        "relation_type": "semantic",
                        "evidence":      [],
                        "count":         0,
                        "first_seen":    _NOW(),
                        "last_seen":     _NOW(),
                    }
                    sem_added += 1

print(f"Semantic relations added: {sem_added}")
print(f"Total relations: {len(relations_db)}")

# ══════════════════════════════════════════════════════════════════════════
# Load Arabic roots and add to concepts
# ══════════════════════════════════════════════════════════════════════════
print("\nLoading Arabic roots...")
roots_path = Path("knowledge/arabic_roots_index.json")
roots_data = {}
arabic_roots_count = 0
if roots_path.exists():
    roots_raw = json.loads(roots_path.read_text(encoding='utf-8'))
    # Only keep roots with freq >= 10 and 3+ char
    for root, info in roots_raw.items():
        if info.get('frequency', 0) >= 10 and len(root) >= 3:
            roots_data[root] = info
    arabic_roots_count = len(roots_data)
    print(f"Meaningful Arabic roots: {arabic_roots_count}")

# ══════════════════════════════════════════════════════════════════════════
# Save
# ══════════════════════════════════════════════════════════════════════════
ckg = {
    "concepts":     concepts_db,
    "relations":    relations_db,
    "arabic_roots": {
        k: {"frequency": v["frequency"], "top_token": v["top_token"]}
        for k, v in roots_data.items()
    },
    "meta": {
        "built_at":          _NOW(),
        "total_ayahs":       len(texts_norm),
        "total_concepts":    len(concepts_db),
        "total_relations":   len(relations_db),
        "arabic_roots":      arabic_roots_count,
        "clusters":          list(RAW_CLUSTERS.keys()),
        "version":           "3.0-expanded",
    }
}

out_path = Path("knowledge/cognitive_graph.json")
out_path.write_text(json.dumps(ckg, ensure_ascii=False, indent=2), encoding='utf-8')
size = out_path.stat().st_size

print(f"\n{'═'*60}")
print(f"✓ CKG saved → {out_path}")
print(f"  المفاهيم   : {len(concepts_db)}")
print(f"  العلاقات   : {len(relations_db)}")
print(f"  الجذور     : {arabic_roots_count}")
print(f"  حجم الملف  : {size:,} bytes  ({size/1024:.1f} KB)")
print(f"\nTop 20 concepts by frequency:")
top20 = sorted(concepts_db.items(), key=lambda x: -x[1]['frequency'])[:20]
for rank, (name, data) in enumerate(top20, 1):
    print(f"  {rank:2d}. {name:<20} freq={data['frequency']:4d}  cluster={data['cluster']}")
