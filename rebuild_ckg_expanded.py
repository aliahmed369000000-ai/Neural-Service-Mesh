_NORM_ALEF = re.compile(r'[أإآٱ]')
_NORM_TA   = re.compile(r'ة$')   # ta-marbuta → ha

def norm(t: str) -> str:
    t = _TASHKEEL.sub('', t)
    t = _NORM_ALEF.sub('ا', t)
    t = _NORM_TA.sub('ه', t)
    return re.sub(r'\s+', ' ', t).strip()

